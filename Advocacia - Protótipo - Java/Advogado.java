public class Advogado extends CadAdvogado {
	private String nomeAdvogado;
	private String cpfAdvogado;
	private String senhaAdvogado;
	private String telefoneAdvogado;
	private String emailAdvogado;
	private String historiaAdvogado;

	public String cadAdvogadoNome(String nome) {
		return nomeAdvogado;
	}

	public String getAdvogadoNome() {
		return nomeAdvogado;
	}

	public String cadAdvogadoCpf(String cpf) {
		return cpfAdvogado;

	}

	public String getAdvogadoCpf() {
		return cpfAdvogado;
	}

	public String cadAdvogadoSenha(String senha) {
		return senhaAdvogado;

	}

	public String getAdvogadoSenha() {
		return senhaAdvogado;
	}

	public String cadAdvogadoTelefone(String telefone) {
		return telefoneAdvogado;

	}

	public String getAdvogadoTelefone() {
		return telefoneAdvogado;
	}

	public String cadAdvogadoEmail(String email) {
		return emailAdvogado;
	}

	public String getAdvogadoEmail() {
		return emailAdvogado;
	}

	public String cadAdvogadoHistoria(String email) {
		return historiaAdvogado;
	}

	public String getAdvogadoHistoria() {
		return historiaAdvogado;
	}

}
